public class BootCampAttendance {
    public static void main(String[] args) {
        int count = 4;
        if (count > 4) {
            System.out.println("The bootcamp has more than 4 attendees.");
        } else if(count == 4) {
            System.out.println("The bootcamp has exactly 4 attendees.");
        } else {
            System.out.println("Bootcamp has less than 4 attendees.");
        }
    }   
}