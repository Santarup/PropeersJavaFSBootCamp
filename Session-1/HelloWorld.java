public class HelloWorld {
    /**
     * when class name and file name are the same, the class is public and main function is defined,
     * inside the same class
     * */ // Multiple line comments
    public static void main(String[] args) {
        // This is a simple Java program that prints "Hello, World!" to the console.
        // The main method is the entry point of the program. // single line comment
        System.out.println("Hello, World!");
    }
    public static void anotherMethod() {
        // This is another method that can be called from the main method or elsewhere.
        System.out.println("This is another method.");
    }
}
